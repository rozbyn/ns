	$temp_arr = [];
	foreach($arr1 as $key=>$val){
		if(array_key_exists($key, $arr2)){
			if($arr2[$key] == $val){
				continue;
			} else {
				$temp_arr[0]['val'][$key] = $arr2[$key];
			}
			
		} else {
			$temp_arr[0]['key'][$key] = $val;
		}
		
	}
	
	foreach($arr2 as $key=>$val){
		if(array_key_exists($key, $arr1)){
			if($arr1[$key] == $val){
				continue;
			}
		} else {
			$temp_arr[1]['key'][$key] = $val;
		}		
	}
	return $temp_arr;
	
	
}

